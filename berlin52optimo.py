def matriz_distancias(datos):
    l = []
    for i in range(len(datos)):
        s = []
        for j in range(len(datos)):
            if not(i == j):
                a1 = abs(datos[i][1]-datos[j][1])
                a2 = abs(datos[i][2]-datos[j][2])
                dis = math.sqrt(a1**2+a2**2)
            else:
                dis = 0
            s.append(dis)
        l.append(s)
    return l
